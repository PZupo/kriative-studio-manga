i18n.use(initReactI18next).init({
  resources,
  lng: localStorage.getItem(LANG_KEY) || 'pt-BR',
  fallbackLng: 'pt-BR',
  interpolation: { escapeValue: false },
});
export const translations = resources;
export type Lang = keyof typeof resources;

// ─────────────────────────────────────────────────────────────
// useI18n — hook global, sem parâmetro
// Uso: const { t, lang, setLang } = useI18n();
// ─────────────────────────────────────────────────────────────
export function useI18n() {
  const { t, i18n: instance } = useTranslation();
  const setLang = (lang: Lang) => {
    instance.changeLanguage(lang);
    localStorage.setItem(LANG_KEY, lang);
  };
  return { t, lang: instance.language as Lang, setLang };
}

export function I18nProvider({ children }: { children: ReactNode }) {
  return <I18nextProvider i18n={i18n}>{children}</I18nextProvider>;
}

export default i18n;
