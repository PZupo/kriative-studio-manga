// ─────────────────────────────────────────────────────────────
// INIT — lê da chave unificada 'k_lang'
// ─────────────────────────────────────────────────────────────
i18n.use(initReactI18next).init({
  resources,
  lng: localStorage.getItem('kriative_lang') || 'pt-BR',
  fallbackLng: 'pt-BR',
  interpolation: { escapeValue: false },
});
export const translations = resources;
export type Lang = keyof typeof resources;

// ─────────────────────────────────────────────────────────────
// useI18n — aceita lang prop opcional
//
// Uso sem prop (Header / componentes globais):
//   const { t, lang, setLang } = useI18n();
//
// Uso com prop (MangaEditor recebe lang do App.tsx):
//   const { t } = useI18n(lang);
//   → força o i18n a usar o idioma do prop imediatamente,
//     sem depender do estado interno do i18next.
// ─────────────────────────────────────────────────────────────
// useI18n — igual Lead Capture e Social Studio, sem parâmetro
export function useI18n() {
  const { t, i18n: instance } = useTranslation();
  const setLang = (lang: Lang) => {
    instance.changeLanguage(lang);
    localStorage.setItem('kriative_lang', lang);
  };
  return { t, lang: instance.language as Lang, setLang };
}

export function I18nProvider({ children }: { children: ReactNode }) {
  return <I18nextProvider i18n={i18n}>{children}</I18nextProvider>;
}

export default i18n;